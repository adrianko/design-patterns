package behavioral.iterator.songs;

public class SongInfo {

    String songName;
    String bandName;
    int yearReleased;

    public SongInfo(String newSongName, String newBandName, int newYearReleased) {
        songName = newSongName;
        bandName = newBandName;
        yearReleased = newYearReleased;
    }
    
}